package org.xdty.phone.number.model;

public class Response {
    Location location;
    String name;
    String type;
    double count;

    public Location getLocation() {
        return location;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public double getCount() {
        return count;
    }
}